public class DataTypes {
    public static void main(String[] args) {
        // primitive data types
        byte b = 127; // -128 ~ +127
        short s = 30_000;// -32,768 ~ +32,767
        int i = 100; // -2B ~ +2B
        float price = 0.25f; // 4 byte 
        double d = 5e-9d; // 8 bytes
        long l = 100000; // 8 bytes
        boolean bo = true; // true or false
        char don = 'a';
      
    }
    
}
