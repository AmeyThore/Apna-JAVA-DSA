public class Product {

    public static int multiple(int a, int b){
        int mult = a * b;
        
        return mult;
    }
    public static void main(String[] args){
        
        int a = 5;
        int b = 20;

        int prod = multiple(a ,b);
        System.out.println("product of a * b = "+prod);
    }
}
    

