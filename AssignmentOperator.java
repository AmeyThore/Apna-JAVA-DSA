public class AssignmentOperator {
    public static void main(String[] args) {
        int a = 10;
        //a = a+10;
        a += 10;
        
        int b = 2;
        b -= 5;

        int c = 5;
        c *= 5;

        int d = 6;
        d /= 3;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);

    }
}
